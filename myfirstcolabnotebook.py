# הצגת קישור לחיץ
def show_clickable_link(link):
    with link_output:
        link_output.clear_output()

        clean_link = link.strip()
        if clean_link == "":
            return

        href = clean_link
        if not (href.startswith("http://") or href.startswith("https://")):
            href = "https://" + href

        display(HTML(f"""
        <div style="
            direction: rtl;
            text-align: right;
            margin-top: -2px;
            margin-bottom: 8px;
            font-family: Arial;
            padding-right: 180px;
        ">
            <a href="{href}" target="_blank">
                לפתיחת הקישור לחצי כאן
            </a>
        </div>
        """))

# בעת בחירת סטודנט
def on_change(change):
    if change['type'] == 'change' and change['name'] == 'value':
        selected_name = change['new']

        if selected_name == 'בחר/י סטודנט':
            details_box.layout.display = 'none'
            txt_first_name.value = ''
            txt_last_name.value = ''
            txt_email.value = ''
            txt_courses.value = ''
            txt_link.value = ''
            txt_favorite.value = ''
            output.clear_output()
            link_output.clear_output()
            return

        for s in students:
            full_name = s['first_name'] + " " + s['last_name']
            if full_name == selected_name:
                txt_first_name.value = s['first_name']
                txt_last_name.value = s['last_name']
                txt_email.value = s['email']
                txt_courses.value = s['courses']
                txt_link.value = s['link']
                txt_favorite.value = ''
                output.clear_output()
                show_clickable_link(s['link'])
                details_box.layout.display = 'flex'
                break

dropdown.observe(on_change)

