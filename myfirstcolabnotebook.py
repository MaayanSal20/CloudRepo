# שמירה בלי כפילויות לפי מייל
students_dict = {}

for line in lines:
    line = line.strip()
    if line != "":
        parts = [p.strip() for p in line.split(',')]

        if len(parts) >= 5:
            student = {
                'first_name': parts[0],
                'last_name': parts[1],
                'email': parts[2],
                'courses': parts[3],
                'link': parts[4]
            }
            students_dict[student['email']] = student

students = list(students_dict.values())

